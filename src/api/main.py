# DAILY GOALS GENERATOR
# =============================================
def generate_daily_goals(db: Session, user_id: int):
    today = date.today()

    # Optional: If you want goals to dynamically update when user submits new data on the same day,
    # you can delete UNCOMPLETED goals for today before generating new ones.
    # db.query(models.Goal).filter(models.Goal.user_id == user_id, models.Goal.date == today, models.Goal.completed == False).delete()
    # db.commit()

    existing_today = db.query(models.Goal).filter(
        models.Goal.user_id == user_id,
        models.Goal.date == today
    ).count()

    if existing_today > 0:
        return  # Goals already exist for today

    # 1. Fetch the latest AI Insights (Recommendations) generated by the engine
    latest_recs = db.query(models.Recommendation).filter(
        models.Recommendation.user_id == user_id
    ).all()

    dynamic_goals = []

    # 2. Extract the specific goals attached to those insights
    for rec in latest_recs:
        if rec.goals:
            try:
                # rec.goals is stored as a JSON string in the database
                goals_list = json.loads(rec.goals)
                dynamic_goals.extend(goals_list)
            except Exception as e:
                print(f"Error parsing goals JSON: {e}")

    # 3. Fallbacks in case the user is perfectly healthy ("Resilient Baseline")
    fallbacks = [
        "Maintain your current healthy habits and routines.",
        "Take a 15-minute walk outdoors for mental clarity.",
        "Use this stable state to focus on high-priority tasks."
    ]

    # 4. Remove duplicates (in case two insights suggest the same goal) while keeping order
    seen = set()
    unique_goals = []
    for g in dynamic_goals + fallbacks:
        if g not in seen:
            unique_goals.append(g)
            seen.add(g)

    # 5. Limit to exactly 3 goals
    final_goals = unique_goals[:3]

    # 6. Save to database
    for text in final_goals:
        db.add(models.Goal(user_id=user_id, date=today, text=text, completed=False))

    db.commit()
