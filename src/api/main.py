# ACHIEVEMENTS CALCULATOR
# =============================================
def calculate_achievements(db: Session, user_id: int, health_score: float, pending_count: int) -> list:
    achievements = []

    if pending_count <= 1:
        achievements.append({"icon": "fas fa-dove", "title": "Early Bird", "description": "1 or fewer pending goals!"})

    streak = calculate_day_streak(db, user_id)
    all_dates = set()
    for row in db.query(func.date(models.Vitals.ts)).filter(models.Vitals.user_id == user_id).distinct().all():
        if row[0]: all_dates.add(row[0] if isinstance(row[0], date) else row[0])
    for row in db.query(models.Lifestyle.date).filter(models.Lifestyle.user_id == user_id).distinct().all():
        if row[0]: all_dates.add(row[0])
    for row in db.query(models.Academic.date).filter(models.Academic.user_id == user_id).distinct().all():
        if row[0]: all_dates.add(row[0])
    for row in db.query(models.Activity.date).filter(models.Activity.user_id == user_id).distinct().all():
        if row[0]: all_dates.add(row[0])

    if all_dates:
        latest_date = max(all_dates)
        days_gap = (date.today() - latest_date).days
        if streak >= 3 and days_gap < 3:
            achievements.append({"icon": "fas fa-chart-line", "title": "Consistent Tracker", "description": f"{streak} day streak!"})

    if health_score > 70:
        achievements.append({"icon": "fas fa-heart", "title": "Health Champion", "description": "Health score above 70!"})

    return achievements


